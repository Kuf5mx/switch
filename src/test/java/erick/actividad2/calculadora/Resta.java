package erick.actividad2.calculadora;

/**
 * Esta clase sirve para realizar restas entre dos numeros.
 */
public class Resta {

    /**
     * Resta dos numeros y regresa el resultado.
     *
     * @param a numero al que se le resta
     * @param b numero que se va a restar
     * @return resultado de a - b
     */
    public static int realizarOperacion(int a, int b) {
        int resultado;
        resultado = a - b;
        return resultado;
    }
}
