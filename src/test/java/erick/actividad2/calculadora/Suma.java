package erick.actividad2.calculadora;

/**
 * Esta clase sirve para realizar sumas entre dos numeros.
 */
public class Suma {

    /**
     * Suma dos numeros y regresa el resultado.
     *
     * @param a primer numero
     * @param b segundo numero
     * @return resultado de a + b
     */
    public static int realizarOperacion(int a, int b) {
        int resultado;
        resultado = a + b;
        return resultado;
    }
}
